func checkKernel(matrix *[][]rune, i int, j int) int {

	if (*matrix)[i][j] != 'A' {
		return 0
	}

	downRight := (*matrix)[i-1][j-1] == 'M' && (*matrix)[i+1][j+1] == 'S' ||
		(*matrix)[i+1][j+1] == 'M' && (*matrix)[i-1][j-1] == 'S'
	upRight := (*matrix)[i+1][j-1] == 'M' && (*matrix)[i-1][j+1] == 'S' ||
		(*matrix)[i-1][j+1] == 'M' && (*matrix)[i+1][j-1] == 'S'

	if downRight && upRight {
		return 1
	}
	return 0
}

func part2(matrix *[][]rune) int {
	ret := 0
	height := len(*matrix)
	width := len((*matrix)[0])

	for i := 1; i < height-1; i++ {
		for j := 1; j < width-1; j++ {
			ret += checkKernel(matrix, i, j)
		}
	}

	return ret
}

