type Direction struct {
	v int
	h int
}

func xmasCount(matrix *[][]rune, flags *[][]rune, i int, j int, idx int, dir Direction) int {
	if i < 0 || i >= len(*matrix) {
		return 0
	}

	if j < 0 || j >= len((*matrix)[0]) {
		return 0
	}

	if idx == 1 && (*matrix)[i][j] != 'M' {
		if (*flags)[i][j] == '░' {
			(*flags)[i][j] = 'x'
		}
		return 0
	}

	if idx == 2 && (*matrix)[i][j] != 'A' {
		if (*flags)[i][j] == '░' {
			(*flags)[i][j] = 'x'
		}
		return 0
	}

	if idx == 3 && (*matrix)[i][j] != 'S' {
		if (*flags)[i][j] == '░' {
			(*flags)[i][j] = 'x'
		}
		return 0
	}

	if idx == 3 && (*matrix)[i][j] == 'S' {
		(*flags)[i][j] = '!'
		return 1
	}

	(*flags)[i][j] = rune('0' + idx)

	newI := i + dir.v
	newJ := j + dir.h
	return xmasCount(matrix, flags, newI, newJ, idx+1, dir)

}

func xmasCountHelper(matrix *[][]rune, flags *[][]rune, i int, j int) int {
	if i < 0 || i >= len(*matrix) {
		return 0
	}

	if j < 0 || j >= len((*matrix)[0]) {
		return 0
	}

	if (*matrix)[i][j] != 'X' {
		return 0
	}
	(*flags)[i][j] = '0'

	vDirs := []int{0, 0, -1, -1, -1, 1, 1, 1}
	hDirs := []int{-1, 1, -1, 0, 1, -1, 0, 1}
	dirs := make([]Direction, 8)

	for dirIndex := range len(vDirs) {
		// fmt.Printf("%d, %d\n", hDirs[dirIndex], vDirs[dirIndex])
		dirs[dirIndex] = Direction{v: vDirs[dirIndex], h: hDirs[dirIndex]}
	}

	ret := 0
	for _, dir := range dirs {
		newI := i + dir.v
		newJ := j + dir.h
		count := xmasCount(matrix, flags, newI, newJ, 1, dir)
		ret += count
		// if count > 0 {
		// 	(*flags)[i][j] = rune('0' + ret)
		// }
	}

	return ret
}

